package com.teamhike.teamhike.Models;

public class GroupTool {
    private String response;
    private String groupUniqueId;
    private String toolGroup;
    private String toolName;
    private String toolNumber;
    private String toolType;

    public String getResponse() {
        return response;
    }

    public void setResponse(String response) {
        this.response = response;
    }

    public String getGroupUniqueId() {
        return groupUniqueId;
    }

    public void setGroupUniqueId(String groupUniqueId) {
        this.groupUniqueId = groupUniqueId;
    }

    public String getToolGroup() {
        return toolGroup;
    }

    public void setToolGroup(String toolGroup) {
        this.toolGroup = toolGroup;
    }

    public String getToolName() {
        return toolName;
    }

    public void setToolName(String toolName) {
        this.toolName = toolName;
    }

    public String getToolNumber() {
        return toolNumber;
    }

    public void setToolNumber(String toolNumber) {
        this.toolNumber = toolNumber;
    }

    public String getToolType() {
        return toolType;
    }

    public void setToolType(String toolType) {
        this.toolType = toolType;
    }
}
